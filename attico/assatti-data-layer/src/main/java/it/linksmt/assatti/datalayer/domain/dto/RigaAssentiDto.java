package it.linksmt.assatti.datalayer.domain.dto;


public class RigaAssentiDto {
	private String numeroDal;
	private String numeroAl;
	private String assenti;
	public String getNumeroDal() {
		return numeroDal;
	}
	public void setNumeroDal(String numeroDal) {
		this.numeroDal = numeroDal;
	}
	public String getNumeroAl() {
		return numeroAl;
	}
	public void setNumeroAl(String numeroAl) {
		this.numeroAl = numeroAl;
	}
	public String getAssenti() {
		return assenti;
	}
	public void setAssenti(String assenti) {
		this.assenti = assenti;
	}
}
